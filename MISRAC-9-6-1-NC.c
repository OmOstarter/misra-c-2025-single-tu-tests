#include <stdint.h>

struct S {
    int x;
    int y;
};
struct T {
    int w;
    struct S s;
    int z;
};

// 錯誤 1: 混用連鎖指定符與位置初始化
struct T t1 = {
    1,        // 位置初始化
    .s.x = 2, // 使用連鎖指定符
    3,        // 位置初始化，混用
};

// 錯誤 2: 子物件內的指定符與外部連鎖指定符不一致
struct T t2 = {
    .s = {
        1,        // 子物件內的省略初始化
    },
    .z = 3,        // 外部使用連鎖指定符
};

// 錯誤 3: 初始化器包含未使用指定符的子物件
struct S s_arr[2] = {
    [0].x = 1, // 使用指定符
    { 2, 3 },  // 未使用指定符
};

// 錯誤 4: 多層初始化器中混用連鎖指定符與省略指定符
struct T t3 = {
    .w = 1,
    .s = {
        .x = 2,
        3, // 子物件內未使用指定符
    },
};
